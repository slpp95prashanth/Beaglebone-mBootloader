#define CPSW_SS_STAT_PORT_EN_P1_STAT_EN_BIT	(1)
#define CPSW_SS_STAT_PORT_EN_P2_STAT_EN_BIT	(2)

/* CPSW_CPDMA */
#define CPSW_CPDMA_SOFT_RESET	(CPSW_CPDMA + 0x1c)
#define CPSW_CPDMA_TX_CONTROL	(CPSW_CPDMA + 0x04)
#define CPSW_CPDMA_RX_CONTROL	(CPSW_CPDMA + 0x14)
#define CPSW_CPDMA_DMASTATUS	(CPSW_CPDMA + 0x24)

/* CPSW_CPDMA_TX_CONTROL */
#define CPSW_CPDMA_TX_CONTROL_TX_EN_BIT	(0)
#define CPSW_CPDMA_RX_CONTROL_RX_EN_BIT	(0)

/* CPSW_CPDMA_DMASTATUS */
#define CPSW_CPDMA_RX_HOST_ERR_0000	"No Err"
#define CPSW_CPDMA_RX_HOST_ERR_0001	"Reserved"
#define CPSW_CPDMA_RX_HOST_ERR_0010	"Ownership bit not set in input buffer"
#define CPSW_CPDMA_RX_HOST_ERR_0011	"Reserved"
#define CPSW_CPDMA_RX_HOST_ERR_0100	"Zero BUffer Pointer"
#define CPSW_CPDMA_RX_HOST_ERR_0101	"Zero buffer length on non-SOP descriptor"
#define CPSW_CPDMA_RX_HOST_ERR_0110	"SOP buffer length not greater than offset"
#define CPSW_CPDMA_RX_HOST_ERR_1111	"Reserved"

#define CPSW_CPDMA_DMASTATUS_IDLE	(1 << 31)

/* CPSW_ALE */
#define CPSW_ALE_CONTROL	(CPSW_ALE + 0x08)
#define CPSW_ALE_PORTCTL0	(CPSW_ALE + 0x40)
#define CPSW_ALE_PORTCTL1	(CPSW_ALE + 0x44)

/* CPSW_ALE CONTROL*/
#define CPSW_ALE_CONTROL_ALE_ENABLE_BIT	(31)
#define CPSW_ALE_CONTROL_ALE_TABLE_CLEAR_BIT	(30)

/* CPSW_ALE_PORTCTL */
#define CPSW_ALE_PORTCTL_PORT_STATE_FORWARD	(3)

/* CPSW_PORT */
#define CPSW_PORT_P0_CPDMA_TX_PRI_MAP	(CPSW_PORT + 0x1c)
#define CPSW_PORT_P0_CPDMA_RX_CH_MAP	(CPSW_PORT + 0x20)
#define CPSW_PORT_P1_SA_LO		(CPSW_PORT + 0x120)
#define CPSW_PORT_P1_SA_HI		(CPSW_PORT + 0x124)

/* CPSW_STATERAM */
#define CPSW_STATERAM_TX0_HDP	(CPSW_STATERAM + 0X00)
#define CPSW_STATERAM_TX1_HDP	(CPSW_STATERAM + 0X04)
#define CPSW_STATERAM_TX2_HDP	(CPSW_STATERAM + 0X08)
#define CPSW_STATERAM_TX3_HDP	(CPSW_STATERAM + 0X0c)
