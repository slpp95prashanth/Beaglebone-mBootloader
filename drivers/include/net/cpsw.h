/* CPSW_MDIO_CONTROL */
#define CPSW_MDIO_CONTROL_ENABLE_BIT	(30)

/* CPSW_SL1 */
#define CPSW_SL1_SOFT_RESET	(CPSW_SL1 + 0x0c)
#define CPSW_SL1_RX_MAXLEN	(CPSW_SL1 + 0x10)
